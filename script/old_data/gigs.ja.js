function createEventElement (s) {
	el = document.createElement('td');
	el.setAttribute('class', 'concerts');
	el.innerHTML = s;
	return el;
}

function createEvent(gig) {
	row = document.createElement('tr');
	row.appendChild ( createEventElement (gig.When) );
	row.appendChild ( createEventElement (gig.Where) );
	row.appendChild ( createEventElement (gig.Who) );
	return row;
}

function populate() {
	for (i = 0; i < upcomingGigs.length; i++) {
		table = document.getElementById ("Upcoming_Interior");
		table.appendChild ( createEvent (upcomingGigs[i]) );
	}

	for (i = 0; i < pastGigs.length; i++) {
		table = document.getElementById ("Past_Interior");
		table.appendChild ( createEvent (pastGigs[i]) );
	}
}

